package com.project.DTO;

public class PointRuleDTO {

	private String ruleSeq;
	private String ruleName;
	private String rulePoint;
	
	public String getRuleSeq() {
		return ruleSeq;
	}
	public void setRuleSeq(String ruleSeq) {
		this.ruleSeq = ruleSeq;
	}
	public String getRuleName() {
		return ruleName;
	}
	public void setRuleName(String ruleName) {
		this.ruleName = ruleName;
	}
	public String getRulePoint() {
		return rulePoint;
	}
	public void setRulePoint(String rulePoint) {
		this.rulePoint = rulePoint;
	}
}
