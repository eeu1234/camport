package com.camport.commuicaiton.model.DTO;

public class CommunicationBoardDTO {

	private int commBoardSeq;
	private String commName;
	private String commContent;
	private String commReadCnt;
	private String commView;
	private String commRegdate;
	private String userId;
	
	
	
	
	
	
	
	public int getCommBoardSeq() {
		return commBoardSeq;
	}
	public void setCommBoardSeq(int commBoardSeq) {
		this.commBoardSeq = commBoardSeq;
	}
	public String getCommName() {
		return commName;
	}
	public void setCommName(String commName) {
		this.commName = commName;
	}
	public String getCommContent() {
		return commContent;
	}
	public void setCommContent(String commContent) {
		this.commContent = commContent;
	}
	public String getCommReadCnt() {
		return commReadCnt;
	}
	public void setCommReadCnt(String commReadCnt) {
		this.commReadCnt = commReadCnt;
	}
	public String getCommView() {
		return commView;
	}
	public void setCommView(String commView) {
		this.commView = commView;
	}
	public String getCommRegdate() {
		return commRegdate;
	}
	public void setCommRegdate(String commRegdate) {
		this.commRegdate = commRegdate;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}

	
	
}
