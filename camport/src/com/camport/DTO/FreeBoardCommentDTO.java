package com.camport.DTO;

public class FreeBoardCommentDTO {

	private int freeBoardCommentSeq;
	private String freeCommentContent;
	private int freeCommentThread;
	private int freeCommentDepth;
	private String freeCommentRegdate;
	private int freeBoardSeq;
	private String userId;
	
	
	
	public int getFreeBoardCommentSeq() {
		return freeBoardCommentSeq;
	}
	public void setFreeBoardCommentSeq(int freeBoardCommentSeq) {
		this.freeBoardCommentSeq = freeBoardCommentSeq;
	}
	public String getFreeCommentContent() {
		return freeCommentContent;
	}
	public void setFreeCommentContent(String freeCommentContent) {
		this.freeCommentContent = freeCommentContent;
	}
	public int getFreeCommentThread() {
		return freeCommentThread;
	}
	public void setFreeCommentThread(int freeCommentThread) {
		this.freeCommentThread = freeCommentThread;
	}
	public int getFreeCommentDepth() {
		return freeCommentDepth;
	}
	public void setFreeCommentDepth(int freeCommentDepth) {
		this.freeCommentDepth = freeCommentDepth;
	}
	public String getFreeCommentRegdate() {
		return freeCommentRegdate;
	}
	public void setFreeCommentRegdate(String freeCommentRegdate) {
		this.freeCommentRegdate = freeCommentRegdate;
	}
	public int getFreeBoardSeq() {
		return freeBoardSeq;
	}
	public void setFreeBoardSeq(int freeBoardSeq) {
		this.freeBoardSeq = freeBoardSeq;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	
}
